    /**
     * Con la información dada por el usuario define
     * un nuevo vehículo para actualizar la información
     */
   private void procesarEntrada() {
      Vehiculo nuevo=null;
        if (jRadioButtonBici.isSelected()){
            nuevo=new Bicicleta(jTextFieldReferencia.getText());
        }else if (jRadioButtonCamion.isSelected()){
            nuevo=new Camion(Float.parseFloat(jTextFieldKMS.getText()), Float.parseFloat(jTextFieldToneladas.getText()), jTextFieldReferencia.getText());
        }else if (jRadioButtonCocheMoto.isSelected()){
            nuevo=new Coche_Moto(Float.parseFloat(jTextFieldKMS.getText()), jTextFieldReferencia.getText());
        }
        gestora.actualizarInformacion(nuevo);
    }
